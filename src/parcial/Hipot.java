    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // CÃ³digo que calcula la hipotenusa
        Double cateto1, cateto2, resultado;
        
        try{
        cateto1 = Double.parseDouble(cat1.getText());
        cateto2 = Double.parseDouble(cat2.getText());
        Hipotenusa calcular = new Hipotenusa();
        resultado = calcular.hipotenusa(cateto1, cateto2);
        result.setText(String.valueOf(resultado));
        }
        catch(Exception errordato){
        JOptionPane.showConfirmDialog(null,"Ingreso de valor no valido");
        cat1.setText("");
        cat2.setText("");
        result.setText("");
        
        }
    }//GEN-LAST:event_jButton1ActionPerformed

